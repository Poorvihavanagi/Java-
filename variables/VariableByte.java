class VariableByte{
	public static void main(String[] args){
		byte age=30;	//Max age 122
		age=18;
				System.out.println(age);
				
     	byte month=10;	//12
		month=6;
			System.out.println(month);
			
		byte daysInMonth=25;	//31
		daysInMonth=10;
			System.out.println(daysInMonth);
			
		byte examMarks=82;		//max 100
		examMarks=100;
			System.out.println(examMarks);
		
		byte senseOrgans=5;	//5 sense organ
		senseOrgans=2;
			System.out.println(senseOrgans);
		
	}
}
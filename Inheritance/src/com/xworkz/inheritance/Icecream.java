// Parent Class
public class Icecream {
    public void prepare() {
        System.out.println("Preparing the ice cream...");
    }

    public void freeze() {
        System.out.println("Freezing the ice cream...");
    }

    public void serve() {
        System.out.println("Serving the ice cream...");
    }

    public void addToppings() {
        System.out.println("Adding toppings to the ice cream...");
    }

    public void eat() {
        System.out.println("Eating the ice cream...");
    }
}

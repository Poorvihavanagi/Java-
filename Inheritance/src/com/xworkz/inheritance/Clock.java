package com.xworkz.inheritance;

public class Clock
{
    public void showTime() {
        System.out.println("Displaying the current time...");
    }

    public void setAlarm() {
        System.out.println("Setting an alarm on the clock...");
    }

    public void stopAlarm() {
        System.out.println("Stopping the alarm...");
    }

    public void checkBattery() {
        System.out.println("Checking the battery level of the clock...");
    }

    public void adjustTime() {
        System.out.println("Adjusting the time on the clock...");
    }
}

package com.xworkz.inheritance;

public class Mammal
{
    public void eat() {
        System.out.println("Mammals eat food...");
    }

    public void sleep() {
        System.out.println("Mammals sleep at night...");
    }

    public void walk() {
        System.out.println("Mammals walk on land...");
    }

    public void breathe() {
        System.out.println("Mammals breathe air...");
    }

    public void giveBirth() {
        System.out.println("Mammals give birth to live young...");
    }
}

package com.xworkz.inheritance;

public class Mug
{
    public void fill() {
        System.out.println("Filling the mug with a beverage...");
    }

    public void drink() {
        System.out.println("Drinking from the mug...");
    }

    public void wash() {
        System.out.println("Washing the mug...");
    }

    public void heat() {
        System.out.println("Heating the mug in the microwave...");
    }

    public void checkMaterial() {
        System.out.println("Checking the material of the mug...");
    }

}

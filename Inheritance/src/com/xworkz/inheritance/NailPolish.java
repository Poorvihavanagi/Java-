package com.xworkz.inheritance;

public class NailPolish {
    public void apply() {
        System.out.println("Applying nail polish...");
    }

    public void remove() {
        System.out.println("Removing nail polish...");
    }

    public void dry() {
        System.out.println("Drying the nail polish...");
    }

    public void checkColor() {
        System.out.println("Checking the color of the nail polish...");
    }

    public void shake() {
        System.out.println("Shaking the nail polish bottle...");
    }
}

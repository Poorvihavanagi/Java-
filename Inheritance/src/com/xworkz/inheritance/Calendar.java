package com.xworkz.inheritance;

public class Calendar {
    public void displayDate() {
        System.out.println("Displaying the current date...");
    }

    public void changeMonth() {
        System.out.println("Changing to the next month...");
    }

    public void markEvent() {
        System.out.println("Marking an important event on the calendar...");
    }

    public void checkHoliday() {
        System.out.println("Checking if today is a holiday...");
    }

    public void flipPage() {
        System.out.println("Flipping to the next page of the calendar...");
    }
}

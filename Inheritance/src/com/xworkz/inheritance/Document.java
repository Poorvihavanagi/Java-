package com.xworkz.inheritance;

public class Document {

    public void edit(){
        System.out.println("Editing document");
    }
    public void draw(){
        System.out.println("Drawing in document");
    }
    public void type(){
        System.out.println("Typing in document");
    }
    public void delete(){
        System.out.println("Deleting content from document");
    }
    public void viewing(){
        System.out.println("Viewing document");
    }
}

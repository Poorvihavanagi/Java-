package com.xworkz.inheritance;

public class SwimmingPool
{
    public void fillWater() {
        System.out.println("Filling the swimming pool with water...");
    }
    public void drainWater() {
        System.out.println("Draining water from the swimming pool...");
    }
    public void clean() {
        System.out.println("Cleaning the swimming pool...");
    }
    public void openPool() {
        System.out.println("Opening the swimming pool for visitors...");
    }
    public void closePool() {
        System.out.println("Closing the swimming pool...");
    }
}

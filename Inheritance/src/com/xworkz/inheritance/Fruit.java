package com.xworkz.inheritance;

public class Fruit {
    public void grow() {
        System.out.println("Growing the fruit...");
    }
    public void ripen() {
        System.out.println("Ripening the fruit...");
    }
    public void harvest() {
        System.out.println("Harvesting the fruit...");
    }
    public void wash() {
        System.out.println("Washing the fruit...");
    }
    public void eat() { System.out.println("Eating the fruit...");
    }
}

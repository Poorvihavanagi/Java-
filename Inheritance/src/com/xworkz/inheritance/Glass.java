package com.xworkz.inheritance;

public class Glass {
    public void fill() {
        System.out.println("Filling the glass...");
    }

    public void drink() {
        System.out.println("Drinking from the glass...");
    }

    public void clean() {
        System.out.println("Cleaning the glass...");
    }

    public void checkMaterial() {
        System.out.println("Checking the material of the glass...");
    }

    public void empty() {
        System.out.println("Emptying the glass...");
    }
}

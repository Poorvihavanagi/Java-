package com.xworkz.inheritance;

public class Sport {

    public void startGame() {
        System.out.println("Starting the sport...");
    }

    public void play() {
        System.out.println("Playing the sport...");
    }

    public void score() {
        System.out.println("Scoring in the sport...");
    }

    public void pause() {
        System.out.println("Pausing the sport...");
    }

    public void endGame() {
        System.out.println("Ending the sport...");
    }
}

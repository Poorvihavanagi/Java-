package com.xworkz.inheritance;

public class Candle
{
    public void light() {
        System.out.println("Lighting the candle");
    }

    public void melt() {
        System.out.println("The candle is melting");
    }

    public void extinguish() {
        System.out.println("Extinguishing the candle");
    }

    public void emitFragrance() {
        System.out.println("The candle is emitting a pleasant fragrance");
    }

    public void provideLight() {
        System.out.println("The candle is providing light");
    }
}

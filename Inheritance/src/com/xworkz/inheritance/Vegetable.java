
package com.xworkz.inheritance;

public class Vegetable {
    public void grow() {
        System.out.println("Vegetable is growing...");
    }

    public void harvest() {
        System.out.println("Harvesting the vegetable...");
    }

    public void wash() {
        System.out.println("Washing the vegetable...");
    }

    public void chop() {
        System.out.println("Chopping the vegetable...");
    }

    public void cook() {
        System.out.println("Cooking the vegetable...");
    }
}

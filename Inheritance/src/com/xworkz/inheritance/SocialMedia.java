package com.xworkz.inheritance;

public class SocialMedia
{
    public void createPost() {
        System.out.println("Creating a new post on social media...");
    }

    public void likePost() {
        System.out.println("Liking a post on social media...");
    }

    public void sharePost() {
        System.out.println("Sharing a post on social media...");
    }

    public void commentOnPost() {
        System.out.println("Commenting on a post...");
    }

    public void sendMessage() {
        System.out.println("Sending a message on social media...");
    }
}

package com.xworkz.inheritance;

public class Jewel {

    public void shine() {
        System.out.println("The jewel is shining brightly...");
    }

    public void wear() {
        System.out.println("Wearing the jewel...");
    }

    public void polish() {
        System.out.println("Polishing the jewel...");
    }

    public void checkPurity() {
        System.out.println("Checking the purity of the jewel...");
    }

    public void storeSafely() {
        System.out.println("Storing the jewel safely...");
    }
}

package com.xworkz.inheritance;

public class Wallet {
    public void open() {
        System.out.println("Opening the wallet...");
    }

    public void close() {
        System.out.println("Closing the wallet...");
    }

    public void storeMoney() {
        System.out.println("Storing money in the wallet...");
    }

    public void checkCapacity() {
        System.out.println("Checking the wallet's capacity...");
    }

    public void carry() {
        System.out.println("Carrying the wallet in the pocket...");
    }
}

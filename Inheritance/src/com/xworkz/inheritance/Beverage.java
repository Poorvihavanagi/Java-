package com.xworkz.inheritance;

public class Beverage {
    public void drink() {
        System.out.println("Drinking the beverage");
    }

    public void pour() {
        System.out.println("Pouring the beverage");
    }

    public void checkTemperature() {
        System.out.println("Checking the temperature of the beverage");
    }

    public void shake() {
        System.out.println("Shaking the beverage");
    }

    public void serve() {
        System.out.println("Serving the beverage");
    }
}

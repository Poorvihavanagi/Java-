package com.xworkz.inheritance;

public class Aquatic {
    public void swim() {
        System.out.println("Aquatic animals swim in water");
    }

    public void breatheUnderwater() {
        System.out.println("Aquatic animals use gills to breathe underwater");
    }

    public void layEggs() {
        System.out.println("Most aquatic animals lay eggs");
    }

    public void adaptToWater() {
        System.out.println("Aquatic animals have adaptations to survive in water");
    }

    public void liveInWater() {
        System.out.println("Aquatic animals spend most of their lives in water");
    }
}

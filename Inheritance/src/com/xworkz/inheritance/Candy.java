package com.xworkz.inheritance;


public class Candy {
    public void taste() {
        System.out.println("Candy tastes sweet");
    }

    public void chew() {
        System.out.println("Chewing the candy slowly");
    }

    public void unwrap() {
        System.out.println("Unwrapping the candy wrapper");
    }

    public void dissolve() {
        System.out.println("Candy dissolves in the mouth");
    }

    public void checkFlavor() {
        System.out.println("Checking the flavor of the candy");
    }
}

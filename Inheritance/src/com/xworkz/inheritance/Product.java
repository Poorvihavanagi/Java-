package com.xworkz.inheritance;

public class Product {
    public void use() {
        System.out.println("Using the product...");
    }

    public void checkExpiry() {
        System.out.println("Checking the product expiry date...");
    }

    public void apply() {
        System.out.println("Applying the product...");
    }

    public void store() {
        System.out.println("Storing the product safely...");
    }

    public void dispose() {
        System.out.println("Disposing of the product properly...");
    }
}

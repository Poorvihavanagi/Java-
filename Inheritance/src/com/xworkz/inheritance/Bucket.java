package com.xworkz.inheritance;

public class Bucket
{
    public void fill() {
        System.out.println("Filling the bucket");
    }

    public void empty() {
        System.out.println("Emptying the bucket");
    }

    public void checkWaterLevel() {
        System.out.println("Checking the water level in the bucket");
    }

    public void carry() {
        System.out.println("Carrying the bucket");
    }

    public void clean() {
        System.out.println("Cleaning the bucket");
    }
}


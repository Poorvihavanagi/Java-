package com.xworkz.inheritance;

public class Train
{
    public void start() {
        System.out.println("Starting the train...");
    }

    public void stop() {
        System.out.println("Stopping the train...");
    }

    public void accelerate() {
        System.out.println("The train is accelerating...");
    }

    public void applyBrakes() {
        System.out.println("Applying brakes to slow down the train...");
    }

    public void honk() {
        System.out.println("The train is honking...");
    }
}

package com.xworkz.inheritance;

public class Medicine {
    public void consume() {
        System.out.println("Consuming the medicine...");
    }

    public void store() {
        System.out.println("Storing medicine in a cool, dry place...");
    }

    public void checkExpiry() {
        System.out.println("Checking the expiry date of the medicine...");
    }

    public void prescribe() {
        System.out.println("Prescribing medicine as per doctor's advice...");
    }

    public void manufacture() {
        System.out.println("Manufacturing medicine in a pharmaceutical company...");
    }
}

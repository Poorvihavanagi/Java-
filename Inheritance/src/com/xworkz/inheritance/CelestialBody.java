package com.xworkz.inheritance;

public class CelestialBody {
    public void shine() {
        System.out.println("The celestial body is shining");
    }

    public void rotate() {
        System.out.println("The celestial body is rotating");
    }

    public void revolve() {
        System.out.println("The celestial body is revolving");
    }

    public void emitLight() {
        System.out.println("The celestial body is emitting light");
    }

    public void existInSpace() {
        System.out.println("The celestial body exists in space");
    }
}

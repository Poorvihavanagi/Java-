package com.xworkz.inheritance;

public class Air {
    public void flow() {
        System.out.println("Air is flowing...");
    }

    public void compress() {
        System.out.println("Compressing the air...");
    }

    public void expand() {
        System.out.println("Expanding the air...");
    }

    public void checkPurity() {
        System.out.println("Checking the purity of the air...");
    }

    public void mix() {
        System.out.println("Mixing different gases in the air...");
    }
}

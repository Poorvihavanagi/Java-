package com.xworkz.inheritance;

public class Entertainment
{
    public void enjoy() {
        System.out.println("Enjoying entertainment...");
    }

    public void watch() {
        System.out.println("Watching something interesting...");
    }

    public void relax() {
        System.out.println("Relaxing while enjoying entertainment...");
    }

    public void engage() {
        System.out.println("Engaging in a fun activity...");
    }

    public void share() {
        System.out.println("Sharing the experience with others...");
    }
}

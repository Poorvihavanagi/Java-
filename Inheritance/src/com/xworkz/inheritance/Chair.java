package com.xworkz.inheritance;

public class Chair {
    public void sit() {
        System.out.println("Sitting on the chair...");
    }

    public void move() {
        System.out.println("Moving the chair...");
    }

    public void fold() {
        System.out.println("Folding the chair...");
    }

    public void adjustHeight() {
        System.out.println("Adjusting the chair height...");
    }

    public void checkMaterial() {
        System.out.println("Checking the material of the chair...");
    }
}

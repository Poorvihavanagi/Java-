package com.xworkz.inheritance;

public class Sticky {
    public void apply() {
        System.out.println("Applying the sticky substance.");
    }

    public void dry() {
        System.out.println("Letting the sticky substance dry.");
    }

    public void bond() {
        System.out.println("The sticky substance is bonding materials.");
    }

    public void checkStickiness() {
        System.out.println("Checking the stickiness level.");
    }

    public void remove() {
        System.out.println("Removing the sticky substance from the surface.");
    }
}

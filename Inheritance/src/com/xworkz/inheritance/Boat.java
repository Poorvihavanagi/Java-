package com.xworkz.inheritance;

public class Boat
{
    public void sail() {
        System.out.println("The boat is sailing on the water...");
    }

    public void anchor() {
        System.out.println("The boat is dropping its anchor...");
    }

    public void loadCargo() {
        System.out.println("Loading cargo onto the boat...");
    }

    public void startEngine() {
        System.out.println("Starting the boat engine...");
    }

    public void stopEngine() {
        System.out.println("Stopping the boat engine...");
    }
}

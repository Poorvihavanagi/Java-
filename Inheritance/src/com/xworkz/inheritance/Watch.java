package com.xworkz.inheritance;

public class Watch {
    public void showTime() {
        System.out.println("Displaying the time...");
    }

    public void setAlarm() {
        System.out.println("Setting an alarm on the watch...");
    }

    public void tick() {
        System.out.println("The watch is ticking...");
    }

    public void checkBattery() {
        System.out.println("Checking the battery level of the watch...");
    }

    public void wear() {
        System.out.println("Wearing the watch...");
    }
}

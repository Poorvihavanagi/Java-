public class Structure {
    public void design() {
        System.out.println("Designing the structure...");
    }

    public void build() {
        System.out.println("Building the structure...");
    }

    public void maintain() {
        System.out.println("Maintaining the structure...");
    }

    public void inspect() {
        System.out.println("Inspecting the structure...");
    }

    public void demolish() {
        System.out.println("Demolishing the structure...");
    }
}

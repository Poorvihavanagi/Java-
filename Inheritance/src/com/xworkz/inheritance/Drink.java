package com.xworkz.inheritance;

public class Drink {

    public void prepare() {
        System.out.println("Preparing the drink");
    }

    public void pour() {
        System.out.println("Pouring the drink into a glass");
    }

    public void serve() {
        System.out.println("Serving the drink");
    }

    public void sip() {
        System.out.println("Sipping the drink");
    }

    public void finish() {
        System.out.println("Finishing the drink");
    }
}

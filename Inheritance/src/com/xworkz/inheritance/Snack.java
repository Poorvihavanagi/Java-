
package com.xworkz.inheritance;

public class Snack {
    public void prepare() {
        System.out.println("Preparing the snack...");
    }

    public void pack() {
        System.out.println("Packing the snack...");
    }

    public void open() {
        System.out.println("Opening the snack pack...");
    }

    public void eat() {
        System.out.println("Eating the snack...");
    }

    public void enjoy() {
        System.out.println("Enjoying the snack...");
    }
}

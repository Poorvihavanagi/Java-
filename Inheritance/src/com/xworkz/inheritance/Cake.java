package com.xworkz.inheritance;

public class Cake
{
    public void bake() {
        System.out.println("Baking the cake");
    }

    public void decorate() {
        System.out.println("Decorating the cake with cream and toppings");
    }

    public void slice() {
        System.out.println("Slicing the cake into pieces");
    }

    public void serve() {
        System.out.println("Serving the cake");
    }

    public void taste() {
        System.out.println("Tasting the delicious cake");
    }
}

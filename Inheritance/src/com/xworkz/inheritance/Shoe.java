package com.xworkz.inheritance;

public class Shoe {
    public void wear() {
        System.out.println("Wearing shoes...");
    }

    public void walk() {
        System.out.println("Walking with shoes...");
    }

    public void remove() {
        System.out.println("Removing shoes...");
    }

    public void polish() {
        System.out.println("Polishing the shoes...");
    }

    public void checkSize() {
        System.out.println("Checking the shoe size...");
    }
}

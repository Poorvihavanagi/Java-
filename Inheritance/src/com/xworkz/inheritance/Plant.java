package com.xworkz.inheritance;

public class Plant {
    public void photosynthesis() {
        System.out.println("The plant is performing photosynthesis...");
    }

    public void absorbWater() {
        System.out.println("The plant is absorbing water through its roots...");
    }

    public void grow() {
        System.out.println("The plant is growing...");
    }

    public void produceOxygen() {
        System.out.println("The plant is releasing oxygen...");
    }

    public void bloom() {
        System.out.println("Some plants bloom with beautiful flowers...");
    }
}

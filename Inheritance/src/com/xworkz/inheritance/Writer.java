package com.xworkz.inheritance;

public class Writer {
    public void write() {
        System.out.println("Writing a piece of work...");
    }

    public void edit() {
        System.out.println("Editing the written work...");
    }

    public void publish() {
        System.out.println("Publishing the written work...");
    }

    public void inspire() {
        System.out.println("Inspiring others through words...");
    }

    public void create() {
        System.out.println("Creating a work of literature...");
    }
}

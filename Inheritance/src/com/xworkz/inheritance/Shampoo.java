package com.xworkz.inheritance;

public class Shampoo {

    public void cleanse() {
        System.out.println("Cleansing the hair with shampoo...");
    }

    public void lather() {
        System.out.println("Shampoo is forming a rich lather...");
    }

    public void rinse() {
        System.out.println("Rinsing the shampoo out of the hair...");
    }

    public void nourish() {
        System.out.println("Shampoo is nourishing the hair...");
    }

    public void fragrance() {
        System.out.println("Shampoo leaves a pleasant fragrance...");
    }
}

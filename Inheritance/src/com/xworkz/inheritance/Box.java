package com.xworkz.inheritance;

public class Box
{
    public void open() {
        System.out.println("Opening the box...");
    }

    public void close() {
        System.out.println("Closing the box...");
    }

    public void storeItems() {
        System.out.println("Storing items in the box...");
    }

    public void checkContents() {
        System.out.println("Checking the contents of the box...");
    }

    public void move() {
        System.out.println("Moving the box...");
    }
}

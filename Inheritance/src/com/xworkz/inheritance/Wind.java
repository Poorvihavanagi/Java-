package com.xworkz.inheritance;

public class Wind {

    public void blow() {
        System.out.println("The wind is blowing...");
    }

    public void changeDirection() {
        System.out.println("The wind is changing direction...");
    }

    public void increaseSpeed() {
        System.out.println("The wind speed is increasing...");
    }

    public void decreaseSpeed() {
        System.out.println("The wind speed is decreasing...");
    }

    public void carryParticles() {
        System.out.println("The wind is carrying dust and particles...");
    }
}

package com.xworkz.inheritance;
public class Prison {
    public void lockUp() {
        System.out.println("Locking up the inmates.");
    }

    public void release() {
        System.out.println("Releasing an inmate after serving the sentence.");
    }

    public void monitor() {
        System.out.println("Monitoring the activities inside the prison.");
    }

    public void enforceRules() {
        System.out.println("Enforcing rules to maintain discipline.");
    }

    public void conductInspection() {
        System.out.println("Conducting a routine inspection in the prison.");
    }
}

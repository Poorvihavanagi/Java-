package com.xworkz.inheritance;

public class Shirt
{
    public void wear() {
        System.out.println("Wearing the shirt...");
    }

    public void wash() {
        System.out.println("Washing the shirt...");
    }

    public void fold() {
        System.out.println("Folding the shirt neatly...");
    }

    public void iron() {
        System.out.println("Ironing the shirt...");
    }

    public void checkSize() {
        System.out.println("Checking the size of the shirt...");
    }
}

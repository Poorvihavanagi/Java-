package com.xworkz.inheritance;

public class Bag {

    public void open() {
        System.out.println("Opening the bag...");
    }

    public void close() {
        System.out.println("Closing the bag...");
    }

    public void carry() {
        System.out.println("Carrying the bag...");
    }

    public void checkCapacity() {
        System.out.println("Checking the bag's capacity...");
    }

    public void organizeItems() {
        System.out.println("Organizing items in the bag...");
    }
}

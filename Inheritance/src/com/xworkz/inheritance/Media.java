package com.xworkz.inheritance;

public class Media {
    public void print() {
        System.out.println("Printing the media content...");
    }

    public void broadcast() {
        System.out.println("Broadcasting media content...");
    }

    public void edit() {
        System.out.println("Editing the media content...");
    }

    public void publish() {
        System.out.println("Publishing the media content...");
    }

    public void distribute() {
        System.out.println("Distributing the media content...");
    }
}
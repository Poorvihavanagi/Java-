package com.xworkz.inheritance;

public class Gym {
    public void openGym() {
        System.out.println("The gym is now open for members...");
    }

    public void closeGym() {
        System.out.println("The gym is now closed...");
    }

    public void provideEquipment() {
        System.out.println("The gym provides various fitness equipment...");
    }

    public void conductTraining() {
        System.out.println("The gym conducts personal training sessions...");
    }

    public void maintainHygiene() {
        System.out.println("The gym ensures cleanliness and hygiene...");
    }
}

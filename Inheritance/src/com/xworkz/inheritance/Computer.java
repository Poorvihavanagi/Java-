package com.xworkz.inheritance;

public class Computer {
    public void process() {
        System.out.println("The computer is processing data...");
    }

    public void storeData() {
        System.out.println("The computer is storing data in memory...");
    }

    public void connectToInternet() {
        System.out.println("The computer is connecting to the internet...");
    }

    public void displayOutput() {
        System.out.println("The computer is displaying output on the screen...");
    }

    public void runSoftware() {
        System.out.println("The computer is running software applications...");
    }
}

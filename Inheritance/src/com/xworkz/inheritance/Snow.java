package com.xworkz.inheritance;

public class Snow
{
    public void fall() {
        System.out.println("Snow is falling...");
    }

    public void melt() {
        System.out.println("Snow is melting...");
    }

    public void accumulate() {
        System.out.println("Snow is accumulating on the ground...");
    }

    public void freeze() {
        System.out.println("Snow is freezing into ice...");
    }

    public void sparkle() {
        System.out.println("Snow is sparkling under the sunlight...");
    }
}

package com.xworkz.inheritance;

public class Flower {

    public void bloom() {
        System.out.println("The flower is blooming...");
    }

    public void smell() {
        System.out.println("The flower has a pleasant fragrance...");
    }

    public void photosynthesize() {
        System.out.println("The flower is photosynthesizing...");
    }

    public void attractBees() {
        System.out.println("The flower is attracting bees...");
    }

    public void wither() {
        System.out.println("The flower is withering...");
    }
}

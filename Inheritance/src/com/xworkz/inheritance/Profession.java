package com.xworkz.inheritance;

public class Profession {
    public void work() {
        System.out.println("Working in a profession...");
    }

    public void getSalary() {
        System.out.println("Receiving salary for work...");
    }

    public void followRules() {
        System.out.println("Following professional ethics and rules...");
    }

    public void gainExperience() {
        System.out.println("Gaining experience over time...");
    }

    public void communicate() {
        System.out.println("Communicating professionally...");
    }
}

package com.xworkz.inheritance;
public class Cloth {
    public void absorb() {
        System.out.println("The cloth absorbs liquid");
    }

    public void clean() {
        System.out.println("The cloth is used for cleaning");
    }

    public void fold() {
        System.out.println("The cloth is folded properly");
    }

    public void dry() {
        System.out.println("The cloth is drying in the sun");
    }

    public void reuse() {
        System.out.println("The cloth can be reused multiple times");
    }
}

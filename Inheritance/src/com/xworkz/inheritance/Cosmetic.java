public class Cosmetic {
    public void apply() {
        System.out.println("Applying the cosmetic product.");
    }

    public void remove() {
        System.out.println("Removing the cosmetic product.");
    }

    public void checkIngredients() {
        System.out.println("Checking the ingredients of the cosmetic.");
    }

    public void smell() {
        System.out.println("Smelling the fragrance of the cosmetic.");
    }

    public void store() {
        System.out.println("Storing the cosmetic in a cool place.");
    }
}

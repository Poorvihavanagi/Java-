package com.xworkz.inheritance;

public class Insect {
        public void description() {
            System.out.println("Insects are small, six-legged creatures.");
        }

        public void move() {
            System.out.println("Insects move by walking, flying, or hopping.");
        }

        public void sound() {
            System.out.println("Insects may make buzzing, chirping, or other sounds.");
        }

        public void food() {
            System.out.println("Insects feed on plants, other insects, or small organisms.");
        }

        public void habitat() {
            System.out.println("Insects live in various environments, including gardens, forests, and even inside homes.");
        }
}


package com.xworkz.inheritance;

public class Coating {
    public void apply() {
        System.out.println("Applying a protective layer...");
    }

    public void dry() {
        System.out.println("Waiting for the coating to dry...");
    }

    public void checkQuality() {
        System.out.println("Checking the quality of the coating...");
    }

    public void remove() {
        System.out.println("Removing the old coating...");
    }

    public void protectSurface() {
        System.out.println("Protecting the surface with the coating...");
    }
}

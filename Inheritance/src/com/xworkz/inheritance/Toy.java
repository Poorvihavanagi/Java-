package com.xworkz.inheritance;

public class Toy {
    public void play() {
        System.out.println("Playing with the toy...");
    }

    public void display() {
        System.out.println("Displaying the toy...");
    }

    public void clean() {
        System.out.println("Cleaning the toy...");
    }

    public void repair() {
        System.out.println("Repairing the toy...");
    }

    public void pack() {
        System.out.println("Packing the toy...");
    }

}

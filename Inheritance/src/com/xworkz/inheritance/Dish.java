package com.xworkz.inheritance;

public class Dish {

    public void wash() {
        System.out.println("Washing the dish");
    }

    public void store() {
        System.out.println("Storing the dish");
    }

    public void use() {
        System.out.println("Using the dish to serve food");
    }

    public void dry() {
        System.out.println("Drying the dish after washing");
    }

    public void checkMaterial() {
        System.out.println("Checking if the dish is ceramic or plastic");
    }
}

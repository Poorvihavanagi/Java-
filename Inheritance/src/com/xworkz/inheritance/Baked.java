package com.xworkz.inheritance;

public class Baked {
    public void prepare() {
        System.out.println("Preparing a baked item...");
    }

    public void bake() {
        System.out.println("Baking at the right temperature...");
    }

    public void coolDown() {
        System.out.println("Cooling down the baked item...");
    }

    public void pack() {
        System.out.println("Packing the baked item for storage...");
    }

    public void serve() {
        System.out.println("Serving the baked item...");
    }
}

package com.xworkz.inheritance;

public class Device
{
    public void powerOn() {
        System.out.println("Powering on the device...");
    }

    public void powerOff() {
        System.out.println("Powering off the device...");
    }

    public void charge() {
        System.out.println("Charging the device...");
    }

    public void connectToNetwork() {
        System.out.println("Connecting the device to the network...");
    }

    public void performTask() {
        System.out.println("Performing a task on the device...");
    }
}

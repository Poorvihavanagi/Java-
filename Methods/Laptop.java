class Laptop {
   
    static void onLaptop() {
        System.out.println("Laptop is turned on.");
    }

    static void restart() {
        System.out.println("Laptop is restarting...");
    }

    static void shutdown() {
        System.out.println("Laptop is shutting down...");
    }

    static void laptopBrand() {
        System.out.println("Laptop brand: Dell");
    }

    static void laptopRAM() {
        System.out.println("Laptop RAM: 16GB");
    }

    static void laptopPrice() {
        System.out.println("Laptop price:12000");
    }

    static void runApplications() {
        System.out.println("Running applications on the laptop...");
    }

    static void browseInternet() {
        System.out.println("Browsing the internet...");
    }

    static void playMedia() {
        System.out.println("Playing media files...");
    }



    public static void main(String[] args) {
        
        Laptop.onLaptop();
        Laptop.laptopBrand();
        Laptop.laptopRAM();
        Laptop.laptopPrice();
    }
}

class TeddyBear{
	public static void color(String furColor)
	{
		System.out.println(furColor);
	}
	
	public static void size(int height){
		System.out.println(height);
	}
	
	public static void isSoft(boolean soft)
	{
		System.out.println(soft);
	}
	
	public static void hug(int duration, boolean squeezable)
	{
		System.out.println(duration);
		System.out.println(squeezable);
	}
	
	public static void play(String childName, String activity)
	{
		System.out.println(childName);
		System.out.println(activity);
	}
	
	public static void customize(String outfit, int length, boolean embroidered)
	{
		System.out.println(outfit);
		System.out.println(length);
		System.out.println(embroidered);
	}
	
	public static void purchase(boolean giftWrapped, char size, String storeName)
	{
		System.out.println(giftWrapped);
		System.out.println(size);
		System.out.println(storeName);
	}
}
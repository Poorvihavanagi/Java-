class Paper{
	
	public static void capacity(String size)
	{
		System.out.println(size);
	}
	 
	public static void usability(boolean ecoFriendly)
	{
		System.out.println(ecoFriendly);
	}
	
	public static void density(double thickness)
	{
		System.out.println(thickness);
	}
	
	public static void fold(int count, char grade)
	{
		System.out.println(count);
		System.out.println(grade);
	}
	
	public static void pieces(int parts, String material)
	{
		System.out.println(parts);
		System.out.println(material);
	}
	
	public static void print(String text, int quantity, String color)
	{
		System.out.println(text);
		System.out.println(quantity);
		System.out.println(color);
	}
	
	public static void modify(int length, double width, String shape)
	{
		System.out.println(length);
		System.out.println(width);
		System.out.println(shape);
	}
}